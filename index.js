// song1
let song1 = new Audio ("mp3/chalate chalate.mp3");
let play1 =document.getElementById("play1");

play1.addEventListener("click", function(){
    if (song1.paused|| song1.currentTime<=0){
        song1.play();
        play1.classList.add("fa-solid fa-play");
        play1.classList.remove("fa-solid fa-play");
    }
    else{
        song1.pause();
        play1.classList.remove("fa-solid fa-play");
        play1.classList.add("fa-solid fa-play");
    }
})
